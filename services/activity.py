# Функция для получения MET значения с поддержкой синонимов
def get_calories_per_minute(activity_type: str, gender: str = None) -> float:
    """
    Возвращает MET значение для типа активности с учетом пола.
    Поддерживает синонимы и возвращает значение по умолчанию для неизвестных типов.
    
    Args:
        activity_type: Тип активности
        gender: Пол ('male', 'female', None) - влияет на базовый метаболизм
    
    Returns:
        MET значение для расчета калорий
    """
    if not activity_type:
        return CALORIES_PER_MINUTE["по_умолчанию"]
    
    activity_type_lower = activity_type.lower()
    
    # Прямое совпадение
    if activity_type_lower in CALORIES_PER_MINUTE:
        base_met = CALORIES_PER_MINUTE[activity_type_lower]
    else:
        # Ищем в синонимах
        base_met = _find_met_by_synonyms(activity_type_lower)
        if not base_met:
            # Частичное совпадение
            base_met = _find_met_by_partial_match(activity_type_lower)
            if not base_met:
                base_met = CALORIES_PER_MINUTE["по_умолчанию"]
    
    # Корректировка MET по полу
    # Женщины обычно сжигают на ~5-10% меньше калорий при той же активности
    if gender == "female":
        base_met *= 0.92  # ~8% меньше для женщин
    
    return base_met

