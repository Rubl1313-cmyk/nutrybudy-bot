def normalize_ingredients(ingredients_list):
    """Применяет нормализатор ко всем ингредиентам в списке."""
    return [normalize_product_name(i) for i in ingredients_list]

def find_matching_dish(ingredients: list, threshold: float = 0.3) -> tuple:
    """
    Ищет блюдо, чей набор ингредиентов наиболее похож на предоставленный.
    Возвращает (название_блюда, оценка_сходства) или (None, 0.0).
    """
    if not ingredients:
        return None, 0.0

    # Нормализуем входные ингредиенты
    input_set = set(normalize_ingredients(ingredients))
    best_match = None
    best_score = 0.0

    for dish_name, dish_ingredients in DISH_DB.items():
        dish_set = set(normalize_ingredients(dish_ingredients))
        intersection = len(input_set & dish_set)
        union = len(input_set | dish_set)
        score = intersection / union if union > 0 else 0.0

        if score > best_score:
            best_score = score
            best_match = dish_name

    if best_score >= threshold:
        return best_match, best_score
    return None, best_score
