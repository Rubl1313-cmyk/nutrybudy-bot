# ========== ФУНКЦИИ ПОИСКА (с поддержкой aliases) ==========

def get_product_variants(base_name: str) -> List[Dict]:
    """
    Возвращает список вариантов продукта по базовому названию.
    Учитывает основное название и синонимы (aliases).
    """
    base_name_lower = base_name.lower().strip()
    variants = []
    seen_keys = set()

    # ✅ УЛУЧШЕНИЕ: Очистка кэша для базовых продуктов
    base_products = ['курица', 'говядина', 'свинина', 'индейка', 'телятина', 'баранина', 
                     'рыба', 'лосось', 'треска', 'сельдь', 'картофель', 'морковь', 'лук', 'капуста']
    
    if base_name_lower in base_products:
        # Очищаем кэш для базовых продуктов чтобы получить актуальные результаты
        cache_keys_to_delete = [k for k in _SEARCH_CACHE.keys() if base_name_lower in k.lower()]
        for key in cache_keys_to_delete:
            del _SEARCH_CACHE[key]
        logger.info(f"🧹 Очищен кэш для базового продукта: {base_name_lower}")

    # Точное совпадение ключа
    if base_name_lower in LOCAL_FOOD_DB:
        item = LOCAL_FOOD_DB[base_name_lower].copy()
        item['key'] = base_name_lower
        variants.append(item)
        seen_keys.add(base_name_lower)

    # Поиск по основному названию (name) и алиасам
    for key, item in LOCAL_FOOD_DB.items():
        if key in seen_keys:
            continue

        # Проверяем name
        if base_name_lower in item['name'].lower():
            variants.append({**item, 'key': key})
            seen_keys.add(key)
            if len(variants) >= 10:
                break
            continue

        # Проверяем алиасы (если есть)
        aliases = item.get('aliases', [])
        if any(base_name_lower in alias.lower() for alias in aliases):
            variants.append({**item, 'key': key})
            seen_keys.add(key)
            if len(variants) >= 10:
                break

    # ✅ УЛУЧШЕНИЕ: Если базовый продукт (курица, говядина и т.д.), ищем все варианты
    logger.info(f"🔍 Проверка расширенного поиска: base_name_lower={base_name_lower}, in_base_products={base_name_lower in base_products}, variants_count={len(variants)}")
    
    if base_name_lower in base_products and len(variants) < 10:
        logger.info(f"🔍 Расширенный поиск для базового продукта: {base_name_lower}")
        # Ищем все продукты, содержащие базовое название
        for key, item in LOCAL_FOOD_DB.items():
            if key in seen_keys:
                continue
                
            # Расширенный поиск по базовому продукту
            item_name_lower = item['name'].lower()
            key_lower = key.lower()
            
            # Проверяем наличие базового продукта в названии
            if (base_name_lower in item_name_lower or base_name_lower in key_lower):
                logger.info(f"✅ Найден вариант: {item['name']} (ключ: {key})")
                variants.append({**item, 'key': key})
                seen_keys.add(key)
                if len(variants) >= 15:  # Увеличим лимит для базовых продуктов
                    break
    else:
        logger.info(f"❌ Расширенный поиск не выполнен. Условия: base_product={base_name_lower in base_products}, variants<10={len(variants) < 10}")

    # Если мало вариантов, добавим частичные совпадения ключа
    if len(variants) < 3:
        for key, item in LOCAL_FOOD_DB.items():
            if key in seen_keys:
                continue
            if base_name_lower in key:
                variants.append({**item, 'key': key})
                seen_keys.add(key)
                if len(variants) >= 10:
                    break

    logger.info(f"🔍 get_product_variants для '{base_name_lower}': найдено {len(variants)} вариантов")
    return variants

async def search_food(query: str, limit: int = 10) -> List[Dict]:
    """Поиск продуктов в локальной базе."""
    query_lower = query.lower().strip()
    results = []

    # Точное совпадение
    if query_lower in LOCAL_FOOD_DB:
        return [LOCAL_FOOD_DB[query_lower]]

    # Частичное совпадение ключа
    for key, item in LOCAL_FOOD_DB.items():
        if query_lower in key or query_lower in item['name'].lower():
            results.append(item)
            if len(results) >= limit:
                break

    # Если мало, ищем по алиасам
    if len(results) < limit:
        for key, item in LOCAL_FOOD_DB.items():
            if any(query_lower in alias.lower() for alias in item.get('aliases', [])):
                if item not in results:
                    results.append(item)
                    if len(results) >= limit:
                        break

    return results
