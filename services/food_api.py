# ========== ФУНКЦИИ ПОИСКА (с поддержкой aliases) ==========

def get_product_variants(base_name: str) -> List[Dict]:
    """
    Возвращает список вариантов продукта по базовому названию.
    Учитывает основное название и синонимы (aliases).
    """
    base_name_lower = base_name.lower().strip()
    variants = []
    seen_keys = set()

    # Точное совпадение ключа
    if base_name_lower in LOCAL_FOOD_DB:
        item = LOCAL_FOOD_DB[base_name_lower].copy()
        item['key'] = base_name_lower
        variants.append(item)
        seen_keys.add(base_name_lower)

    # Поиск по основному названию (name) и алиасам
    for key, item in LOCAL_FOOD_DB.items():
        if key in seen_keys:
            continue

        # Проверяем name
        if base_name_lower in item['name'].lower():
            variants.append({**item, 'key': key})
            seen_keys.add(key)
            if len(variants) >= 10:
                break
            continue

        # Проверяем алиасы (если есть)
        aliases = item.get('aliases', [])
        if any(base_name_lower in alias.lower() for alias in aliases):
            variants.append({**item, 'key': key})
            seen_keys.add(key)
            if len(variants) >= 10:
                break

    # Если мало вариантов, добавим частичные совпадения ключа
    if len(variants) < 3:
        for key, item in LOCAL_FOOD_DB.items():
            if key in seen_keys:
                continue
            if base_name_lower in key:
                variants.append({**item, 'key': key})
                seen_keys.add(key)
                if len(variants) >= 10:
                    break

    return variants

async def search_food(query: str, limit: int = 10) -> List[Dict]:
    """Поиск продуктов в локальной базе."""
    query_lower = query.lower().strip()
    results = []

    # Точное совпадение
    if query_lower in LOCAL_FOOD_DB:
        return [LOCAL_FOOD_DB[query_lower]]

    # Частичное совпадение ключа
    for key, item in LOCAL_FOOD_DB.items():
        if query_lower in key or query_lower in item['name'].lower():
            results.append(item)
            if len(results) >= limit:
                break

    # Если мало, ищем по алиасам
    if len(results) < limit:
        for key, item in LOCAL_FOOD_DB.items():
            if any(query_lower in alias.lower() for alias in item.get('aliases', [])):
                if item not in results:
                    results.append(item)
                    if len(results) >= limit:
                        break

    return results
