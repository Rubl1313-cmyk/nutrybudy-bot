async def translate_to_russian(text: str) -> str:
    """
    Переводит текст с английского на русский.
    Сначала проверяет локальный словарь (300+ наименований),
    затем вызывает MyMemory API для редких слов.
    """
    # Нормализация текста
    original = text.strip()
    text_lower = original.lower()

    # Проверка в словаре
    if text_lower in COMMON_TRANSLATIONS:
        translated = COMMON_TRANSLATIONS[text_lower]
        logger.info(f"🔄 Using cached translation: '{original}' → '{translated}'")
        return translated

    # Если не нашли в словаре, пробуем API
    try:
        url = "https://api.mymemory.translated.net/get"
        params = {
            "q": original,
            "langpair": "en|ru"
        }
        async with aiohttp.ClientSession() as session:
            async with session.get(url, params=params, timeout=10) as resp:
                if resp.status == 200:
                    data = await resp.json()
                    translated = data.get('responseData', {}).get('translatedText', original)
                    if translated != original and translated:
                        logger.info(f"🔄 API translation: '{original}' → '{translated}'")
                        return translated
                    else:
                        logger.warning(f"⚠️ Translation API returned unchanged text for '{original}'")
                        return original
                else:
                    logger.warning(f"⚠️ Translation API error: {resp.status}")
                    return original
    except Exception as e:
        logger.error(f"❌ Translation error: {e}")
        return original


async def extract_food_items(description: str) -> list:
    """
    Извлекает отдельные продукты из описания.
    Заменяет союзы на запятые и разбивает.
    """
    # Заменяем 'and', 'with', 'in', 'from', 'for', 'of' на запятые
    text = re.sub(r'\b(and|with|in|from|for|of|on|at|by)\b', ',', description.lower())
    # Разбиваем по запятым и точкам с запятой
    items = [item.strip() for item in re.split(r'[,;]', text) if item.strip()]
    # Убираем возможные артикли в начале
    cleaned = []
    for item in items:
        # Удаляем артикли и лишние пробелы
        item = re.sub(r'^(a|an|the)\s+', '', item)
        item = re.sub(r'\s+', ' ', item).strip()
        cleaned.append(item)
    return cleaned
